     public void load(){
        DefaultTableModel q = new DefaultTableModel();
        q.addColumn("No");
        q.addColumn("ID User");
        q.addColumn("Username");
        q.addColumn("Password");
        q.addColumn("Tipe User");
        q.addColumn("Email");
        q.addColumn("Nomor Telepon");

        
        try {
            int no=1;
            String sql = "SELECT * FROM user";
            Statement stm= (Statement) koneksi.getConnection().createStatement();
            ResultSet res=stm.executeQuery(sql);
            while(res.next()){
                q.addRow(new Object[]{no++,res.getString(1),res.getString(2),res.getString(3),res.getString(4),res.getString(5),res.getString(6)});
            }
            table_user.setModel(q);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

