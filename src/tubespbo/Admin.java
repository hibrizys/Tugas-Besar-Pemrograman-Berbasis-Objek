    private void jComboBox2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox2ActionPerformed
        // TODO add your handling code here:
        String item = (String)jComboBox2.getSelectedItem();
        try {
            String sql = "SELECT * FROM kurir WHERE no_resi='"+item+"'";
            Statement stm = (Statement) koneksi.getConnection().createStatement();
            ResultSet res = stm.executeQuery(sql);
            while (res.next()){
                jLabel8.setText(res.getString("no_resi"));
                jLabel10.setText(res.getString("status_terkirim"));
                if ("2".equals(res.getString("ID_user"))){
                    jLabel3.setText("Izy");
                }
            }
        } catch (Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jComboBox2ActionPerformed

